#测试for循环
for x in (20,30,40):
    print (x*3)

for y in "abcdef":
    print(y)

#遍历字典
d={"name":"SKY","age":"18","address":"陕西西安"}
for x in d:
    print (x)

for x in d.keys():
    print(x)

for x in d.values():
    print(x)

for x in d.items():
    print(x)

